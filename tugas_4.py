# menentukan hari

hari_ini = int(input("Hari Ke: "))

if(hari_ini == 1):
    print("ini hari senin")
elif(hari_ini == 2):
    print("ini hari selasa")
elif(hari_ini == 3):
    print("ini hari rabu")
elif(hari_ini == 5):
    print("ini hari jumat")
elif(hari_ini == 6):
    print("ini hari sabtu")
elif(hari_ini == 7):
    print("ini hari minggu")
else:
    print("Hari tidak di termukan")
